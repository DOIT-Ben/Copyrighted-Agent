"""
软著审查规范要求
严格遵循中国版权保护中心《计算机软件著作权登记指南》
"""

# 完整的审查规范清单
REVIEW_SPECIFICATIONS = """
## 一、申请材料规范要求

### 源代码要求
- 提交前后各连续 30 页，每页≥50 行
- 不足 60 页则全交
- 页眉注明申请人名称与软件全称
- 去除空行，确保每页代码行数达标

### 文档要求
- 用户手册/设计说明书前后各 30 页，每页≥30 行
- 需图文并茂，含功能说明、操作流程、界面截图
- 功能描述与实际软件一致

### 身份证明
- 个人申请需身份证复印件（手写签名，不可用电子签）
- 企业申请需营业执照副本（加盖公章）

## 二、审查注意事项（关键）

### 日期逻辑验证
- 开发完成日期不得早于材料中出现的任何日期
- 首次发表日期不得早于开发完成日期
- 协议签署日期应在合理范围内

### 信息一致性检查
- 软件名称在申请表、源码、文档中必须完全一致
- 版本号格式统一
- 著作权人信息准确无误

### 禁止虚假申报
- 严禁抄袭、拼凑、无实质功能的申请
- 源码应体现真实的软件功能
- 文档描述的功能必须在源码中有对应实现

### 补正机制
- 若材料不符，审查员将发出《补正通知书》
- 须在 30 日内按要求补正
- 逾期视为撤回

## 三、顺利通过技巧

### 代码整理
- 确保代码清晰可读，去除敏感信息
- 前后 30 页应包含核心功能代码
- 避免大量重复代码或空行
- 注释比例适当（≤30%）

### 文档编写
- 图文并茂，每页都有界面截图描述
- 功能描述清晰，操作流程完整
- 确保文档与软件实际功能一致

### 信息核对
- 软件名称在所有材料中完全一致
- 版本号格式统一
- 著作权人信息准确无误
"""

# 合作协议审查专用规范
AGREEMENT_REVIEW_SPEC = """
## 合作协议审查规范

### 必检项
1. **签定 vs 签订** - 法律文件必须使用"签订"，"签定"是错误用法
2. **条款编号连续性** - 检查条款编号是否连续完整
3. **各方数量一致性** - 签约方数量应在全文中保持一致
4. **日期一致性** - 所有日期应为同一日期或在合理范围内
5. **金额/地址等敏感信息** - 已脱敏处理

### 逻辑一致性检查
1. 条款中提到的人数/数量是否与签约方数量一致
2. 各条款的逻辑是否连贯（先定义再引用）
3. 权利和义务是否对等
4. 违约责任是否明确

### 格式规范
1. 标点符号使用正确
2. 句子结构完整
3. 无错别字
"""

def get_full_specifications():
    """获取完整规范"""
    return REVIEW_SPECIFICATIONS

def get_agreement_spec():
    """获取合作协议审查规范"""
    return AGREEMENT_REVIEW_SPEC

